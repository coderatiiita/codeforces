package com.example.dp;

import java.util.Scanner;

public class FillingDiamonds {
    public static void main(String[] args) {
        int t;
        Scanner sc = new Scanner(System.in);
        t = sc.nextInt();

        while(t-->0) {
            int n;
            n = sc.nextInt();
            System.out.println(n);
        }

    }
}
